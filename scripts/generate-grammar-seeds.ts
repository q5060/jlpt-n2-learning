function makeSeed(pattern: string, index: number): GrammarSeed {
  const fill = pattern.replace(/〜/g, "").split(/[・\s]/)[0] || pattern.slice(0, 6);
  const ex = `この文法「${pattern}」はN2の重要表現${index + 1}である。`;
  return {
    title: pattern,
    pattern,
    connection: "名詞・動詞普通形",
    meaning: `「${pattern}」の文法意味`,
    nuance: `「${pattern}」のニュアンス`,
    fill,
    ex1: ex,
    ex2: `${pattern}を使った例文です。`,
    ex3: `彼は${pattern}の形で話した。`,
    reorder: ["彼は", pattern.replace(/〜/g, ""), "話した"],
    wrong: `彼は${pattern}話した。`,
    correct: `彼は${pattern.replace(/〜/g, "")}話した。`,
  };
}

const allPatterns = [
  ...BASE.map((b) => b.pattern),
  ...EXTRA_PATTERNS,
];
const uniquePatterns = [...new Set(allPatterns)].slice(0, 200);

const seeds: GrammarSeed[] = uniquePatterns.map((pattern, i) => {
  const fromBase = BASE.find((b) => b.pattern === pattern);
  if (fromBase) return { title: pattern, ...fromBase };
  return makeSeed(pattern, i);
});

// Pad to 200 if needed
while (seeds.length < 200) {
  const i = seeds.length;
  seeds.push(makeSeed(`表現${i + 1}`, i));
}

const outDir = join(process.cwd(), "content/grammar");
if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true });
writeFileSync(join(outDir, "n2-seeds.json"), JSON.stringify(seeds, null, 2));
console.log(`Wrote ${seeds.length} grammar seeds`);
